package com.adyen.terminal.security;

/**
 * An abstraction for certificate information used in validation. This interface decouples the
 * validator from JDK certificate classes, making testing easier and compatible with Java 21+
 * where certain certificate classes are sealed.
 */
public interface CertificateInfo {
  /**
   * Returns the subject distinguished name of the certificate.
   *
   * @return the subject DN as a string.
   */
  String getSubjectName();
}
