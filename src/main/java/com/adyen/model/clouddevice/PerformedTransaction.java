package com.adyen.model.clouddevice;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonPropertyOrder;
import io.swagger.v3.oas.annotations.media.Schema;
import java.math.BigDecimal;
import java.util.ArrayList;
import java.util.List;

/** Result of performed transactions. */
@JsonPropertyOrder({
    "Response",
    "SaleData",
    "POIData",
    "PaymentResult",
    "LoyaltyResult",
    "ReversedAmount"
})
public class PerformedTransaction {

  @JsonProperty("Response")
  @Schema(description = "Result of a message request processing.")
  protected Response response;

  @JsonProperty("SaleData")
  @Schema(
      description =
          "Data related to the Sale System. --Rule: If a request has been generated by the Sale system.")
  protected SaleData saleData;

  @JsonProperty("POIData")
  @Schema(description = "Data related to the POI System.")
  protected POIData poiData;

  @JsonProperty("PaymentResult")
  @Schema(
      description =
          "Data related to the result of a processed payment transaction. --Rule: If a Payment transaction has been performed and one (or several) data element is present")
  protected PaymentResult paymentResult;

  @JsonProperty("LoyaltyResult")
  @Schema(
      description =
          "Data related to the result of a processed loyalty transaction. --Rule: If a Loyalty transaction has been performed alone or with the Payment transaction.")
  protected List<LoyaltyResult> loyaltyResult;

  @JsonProperty("ReversedAmount")
  @Schema(
      description =
          "Amount of the payment or loyalty to reverse.. --Rule: If a transaction Reversal has been performed.")
  protected BigDecimal reversedAmount;

  /**
   * Gets response.
   *
   * @return the response
   */
  public Response getResponse() {
    return response;
  }

  /**
   * Sets response.
   *
   * @param response the response
   */
  public void setResponse(Response response) {
    this.response = response;
  }

  /**
   * Gets sale data.
   *
   * @return the sale data
   */
  public SaleData getSaleData() {
    return saleData;
  }

  /**
   * Sets sale data.
   *
   * @param saleData the sale data
   */
  public void setSaleData(SaleData saleData) {
    this.saleData = saleData;
  }

  /**
   * Gets poi data.
   *
   * @return the poi data
   */
  public POIData getPOIData() {
    return poiData;
  }

  /**
   * Sets poi data.
   *
   * @param poiData the poi data
   */
  public void setPOIData(POIData poiData) {
    this.poiData = poiData;
  }

  /**
   * Gets payment result.
   *
   * @return the payment result
   */
  public PaymentResult getPaymentResult() {
    return paymentResult;
  }

  /**
   * Sets payment result.
   *
   * @param paymentResult the payment result
   */
  public void setPaymentResult(PaymentResult paymentResult) {
    this.paymentResult = paymentResult;
  }

  /**
   * Gets loyalty result.
   *
   * @return the loyalty result
   */
  public List<LoyaltyResult> getLoyaltyResult() {
    if (loyaltyResult == null) {
      loyaltyResult = new ArrayList<>();
    }
    return this.loyaltyResult;
  }

  /**
   * Sets loyalty result.
   *
   * @param loyaltyResult the loyalty result
   */
  public void setLoyaltyResult(List<LoyaltyResult> loyaltyResult) {
    this.loyaltyResult = loyaltyResult;
  }

  /**
   * Gets reversed amount.
   *
   * @return the reversed amount
   */
  public BigDecimal getReversedAmount() {
    return reversedAmount;
  }

  /**
   * Sets reversed amount.
   *
   * @param reversedAmount the reversed amount
   */
  public void setReversedAmount(BigDecimal reversedAmount) {
    this.reversedAmount = reversedAmount;
  }
}